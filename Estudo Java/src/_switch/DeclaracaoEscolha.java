public class DeclaracaoEscolha {

	public static void main(String[] args) {
		int data = 3;	
		switch(data){
			case 1: 
				System.out.println("Dia 1");
				break;
			case 2:
				System.out.println("Dia 2");
				break;
			case 3:
				System.out.println("Dia 3");
				break;
		}
	}
}
